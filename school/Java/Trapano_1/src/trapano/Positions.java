package trapano;

import java.util.*;
/**
 *
 * @author Studente
 */
public class Positions {
    public int x,y;
       
    public Positions(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public void setXY(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
}
